# Escriba un programa que le pregunte al usuario el largo y el ancho de una habitación. 
# Una vez que los valores han sido leídos, su programa debe calcular y desplegar el área de la habitación. 
# El largo y ancho debe ser introducido con punto flotante (decimal). 
# Incluya las unidades (metros) en su mensaje de entrada y salida.


largo = float(input ('inserta el largo de la habitacion: '))
ancho = float(input ('inserta el ancho de la habitacion: '))

print ('\n El area de la habitación es de', largo*ancho, 'Metros cuadrados')

